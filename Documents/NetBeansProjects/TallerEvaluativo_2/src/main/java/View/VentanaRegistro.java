    private void cargarTablaHuespedes(List<Huesped> lista) {
        DefaultTableModel model = (DefaultTableModel) TablaHuesped.getModel();
        model.setRowCount(0); // Limpia la tabla
        for (Huesped h : lista) {
            model.addRow(new Object[]{h.getNombre(), h.getDocumento(), h.getCorreo(), h.getTelefono()});
            }
    }
