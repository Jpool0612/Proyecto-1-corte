    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    try {
        String nombre = txtNombre.getText().trim();
        String documento = txtDocumento.getText().trim();
        String correo = txtCorreo.getText().trim();
        String telefono = txtTelefono.getText().trim();

        ControllerHuesped controller = new ControllerHuesped();
        boolean guardado = controller.registrarHuesped(nombre, documento, correo, telefono);

        if (guardado) {
            JOptionPane.showMessageDialog(this, "Huésped registrado con éxito.");
            cargarTablaHuespedes(controller.listarHuespedes());
            limpiarCamposHuesped();
        }
    } catch (DatoInvalidoException e) {
        JOptionPane.showMessageDialog(this, e.getMessage(), "Error de datos", JOptionPane.ERROR_MESSAGE);
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarActionPerformed

